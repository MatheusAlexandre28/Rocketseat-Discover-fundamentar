// # Programação
// Imperativa e Declarativa

// Função que eleva o numero ao quadrado

// Imperativo: Faça da seguinte forma
let number = 2

function square() {
  return number * number 
}

number = square()

// Declarativo: O que fazer e não como fazer.
const square = n => n * n 


